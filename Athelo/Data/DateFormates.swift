import Foundation

enum AtheloDateFormates: String {
    
    case dd_MMM_EEEE        = "dd MMM, EEEE"
    case MM_dd_yyyy         = "MM/dd/yyyy"
    case MM_dd_yyyy_hh_mm_a = "MM/dd/yyyy hh:mm a"
    case hh_mm_a            = "hh:mm a"
    case yyyy_MM_dd         = "yyyy-MM-dd"
    case HH_mm_ss           = "HH:mm:ss"
    
}
